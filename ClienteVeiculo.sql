CREATE DATABASE EXERC;
USE EXERC;

CREATE TABLE CLIENTE(
	ID_C INT auto_increment,
	NOME VARCHAR(50),
    IDADE INT,
    CNH VARCHAR(50),
    METODO_PAG VARCHAR(50),
    PRIMARY KEY (ID_C)
);

CREATE TABLE VEICULOS(
	ID_V INT auto_increment,
	NOME VARCHAR(50),
    PLACA VARCHAR(50),
    MODELO VARCHAR(50),
    COR VARCHAR(50),
    PRIMARY KEY (ID_V)
);

