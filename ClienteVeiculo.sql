#INNER
SELECT a.NOME, b.NOME
FROM CLIENTE as a
INNER JOIN VEICULOS as b
					on a.NOME = b.NOME;

#LEFT
SELECT a.NOME, b.NOME
FROM CLIENTE as a
LEFT JOIN VEICULOS as b
					on a.NOME = b.NOME;
                    
#RIGHT
SELECT a.NOME, b.NOME
FROM CLIENTE as a
RIGHT JOIN VEICULOS as b
					on a.NOME = b.NOME;  
                    
#OUTER
